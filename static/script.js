function searchClubs() {
    const input = document.getElementById("club-search");
    const dataList = document.getElementById("club-list");

    // Clear previous suggestions
    dataList.innerHTML = '';

    // Filter club names based on user input
    const searchTerm = input.value.toLowerCase();
    const matchingClubs = clubNames.filter(club => club.toLowerCase().includes(searchTerm));

    // Add matching clubs to the datalist
    matchingClubs.forEach(club => {
        const option = document.createElement('option');
        option.value = club;
        dataList.appendChild(option);
    });
}
